import { useState } from "react";
import PropTypes from "prop-types";

const containerStyle = {
  display: "flex",
  alignItems: "center",
  gap: "16px",
};

const starContainerStyle = {
  display: "flex",
  gap: "4px",
};

//Destructuring the props and setting the default value if needed
const StarRating = ({
  maxRating = 5,
  color = "#fcc419",
  size = 48,
  defaultRating = 0,
  onSetRating,
}) => {
  const [rating, setRating] = useState(defaultRating);
  const [tempRating, setTempRating] = useState(0);
  const textStyle = {
    lineHeight: "1",
    margin: "0",
    color: color,
    fontSize: `${size / 1.5}px`,
  };
  //Creating array of stars with default length of 5
  const stars = Array.from({ length: maxRating }, (_, i) => (
    <Star
      key={i}
      onRate={() => handleRating(i + 1)}
      /*When we hover on a star we want to update the tempRatung value and when we hover out the value of tempRating will be set back to zero. */
      /*the value of full will be evaluated based on the temp rating (if it exists) if it doesn't exist than based on the value of rating*/
      full={tempRating ? tempRating >= i + 1 : rating >= i + 1}
      onHoverIn={() => setTempRating(i + 1)}
      onHoverOut={() => setTempRating(0)}
      color={color}
      size={size}
    />
  ));
  // Full - if condition true render full star, if false render empty star. If there is temporary rating then do this with the temporary rating (grater than 0) else do it with rating
  //When user clicks on a star the value of rating will be updated to the value od i + 1
  const handleRating = (rating) => {
    setRating(rating);
    onSetRating(rating); // We also update the state from the external component that we accepted as prop
  };
  return (
    <div style={containerStyle}>
      <div style={starContainerStyle}>{stars}</div>
      <p style={textStyle}>{tempRating || rating || ""}</p>
    </div>
  );
};
//Setting PropTypes for StarRating component
StarRating.propTypes = {
  maxRating: PropTypes.number,
  defaultRating: PropTypes.number,
  color: PropTypes.string,
  size: PropTypes.number,
  onSetRating: PropTypes.func,
};
const Star = ({ onRate, full, onHoverIn, onHoverOut, color, size }) => {
  const starStyle = {
    width: `${size}px`,
    height: `${size}px`,
    display: "block",
    cursor: "pointer",
  };
  return (
    <span
      role="button"
      style={starStyle}
      onClick={onRate}
      onMouseEnter={onHoverIn}
      onMouseLeave={onHoverOut}
    >
      {/*Conditional rendering: if full returns true then full star will be rendered, if false is returned than empty star will be rendered*/}
      {full ? (
        <svg
          xmlns="http://www.w3.org/2000/svg"
          viewBox="0 0 20 20"
          fill={color}
          stroke={color}
        >
          <path d="M9.049 2.927c.3-.921 1.603-.921 1.902 0l1.07 3.292a1 1 0 00.95.69h3.462c.969 0 1.371 1.24.588 1.81l-2.8 2.034a1 1 0 00-.364 1.118l1.07 3.292c.3.921-.755 1.688-1.54 1.118l-2.8-2.034a1 1 0 00-1.175 0l-2.8 2.034c-.784.57-1.838-.197-1.539-1.118l1.07-3.292a1 1 0 00-.364-1.118L2.98 8.72c-.783-.57-.38-1.81.588-1.81h3.461a1 1 0 00.951-.69l1.07-3.292z" />
        </svg>
      ) : (
        <svg
          xmlns="http://www.w3.org/2000/svg"
          fill="none"
          viewBox="0 0 24 24"
          stroke={color}
        >
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            strokeWidth="2" // Fix typo here: Use double quotes, not curly braces
            d="M11.049 2.927c.3-.921 1.603-.921 1.902 0l1.519 4.674a1 1 0 00.95.69h4.915c.969 0 1.371 1.24.588 1.81l-3.976 2.888a1 1 0 00-.363 1.118l1.518 4.674c.3.922-.755 1.688-1.538 1.118l-3.976-2.888a1 1 0 00-1.176 0l-3.976 2.888c-.783.57-1.838-.197-1.538-1.118l1.518-4.674a1 1 0 00-.363-1.118l-3.976-2.888c-.784-.57-.38-1.81.588-1.81h4.914a1 1 0 00.951-.69l1.519-4.674z"
          />
        </svg>
      )}
    </span>
  );
};

export default StarRating;
